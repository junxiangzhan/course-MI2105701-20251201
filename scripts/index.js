// A fake fetch function to simulate server API calls.

const fakeServer = new class {
    users;

    constructor() {
        const rawUsers = localStorage.getItem('users');
        this.users = rawUsers ? JSON.parse(rawUsers) : [];
    }

    // fetch function to simulate server API calls.

    async fetch(url, { method = 'get', body: bodyRaw = null } = {}) {
        const body = bodyRaw ? bodyRaw.toString() : null;

        switch (url) {
            case '/api/verify':
                if (method == 'post')
                    return this.response(this.getUserInformation(JSON.parse(body)));
            case '/api/create':
                if (method == 'post')
                    return this.response(this.createAccount(JSON.parse(body)));
            case '/api/update':
                if (method == 'post')
                    return this.response(this.updateInfromation(JSON.parse(body)));
            case '/api/check':
                if (method == 'post')
                    return this.response(this.checkDuplicate(JSON.parse(body)));
            case '/api/get-votes':
                if (method == 'get')
                    return this.response(this.getVotes());
        }

        throw new Error('API not found');
    }

    response(data) {
        return {
            async json() {
                return data;
            }
        };
    }

    // Server-side helper functions.

    writeUsers() {
        localStorage.setItem('users', JSON.stringify(this.users));
    }

    // APIS

    checkDuplicate(target) {
        for (const user of this.users) {
            if (
                user.phoneNumber == target.phoneNumber ||
                user.email == target.email
            )
                return true;
        }
        return false;
    }

    createAccount(target) {
        // Check all required columns
        if (!target.name)
            throw new Error('Username is required');
        if (!target.phoneNumber)
            throw new Error('Phone number is required');
        if (!target.email)
            throw new Error('Email is required');


        if (this.checkDuplicate(target))
            throw new Error('Phone number or email is duplicated');

        const userObject = { ...target };
        this.users.push(userObject);
        this.writeUsers();

        return userObject;
    }

    updateInfromation(target) {
        Object.assign(this.getUserInformation(target), target);
        this.writeUsers();
    }


    getUserInformation(target) {
        for (const user of this.users) {
            if (user.phoneNumber == target.phoneNumber && user.email == target.email) {
                return user;
            }
        }

        throw new Error('User not found');
    }

    getVotes() {
        const votes = [2131, 2510, 1936, 1369, 1925]; // Example vote counts

        for (const user of this.users) {
            if (user.vote)
                votes[user.vote] += 1;
        }

        return votes;
    }
};

